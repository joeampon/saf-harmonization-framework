def get_dataframe() -> pd.DataFrame:
    """Return literature database as a pandas DataFrame."""
    return pd.DataFrame(STUDIES)


def summary() -> None:
    """Print summary statistics of the literature database."""
    df = get_dataframe()
    print(f"\n{'='*60}")
    print("LITERATURE DATABASE SUMMARY")
    print(f"{'='*60}")
    print(f"Total studies : {len(df)}")
    print(f"Year range    : {df['year'].min()} – {df['year'].max()}")
    print(f"\nPathway distribution:")
    for pathway, grp in df.groupby("pathway"):
        print(f"  {pathway:8s}: {len(grp):2d} studies  "
              f"MFSP range (raw) = {grp['mfsp_raw'].min():.2f}–{grp['mfsp_raw'].max():.2f} "
              f"({grp['mfsp_unit'].iloc[0]})")
    print(f"\nAllocation methods:")
    for method, cnt in df["allocation"].value_counts().items():
        print(f"  {method:20s}: {cnt} studies")
    print(f"\nSystem boundaries:")
    for bnd, cnt in df["boundary"].value_counts().items():
        print(f"  {bnd:12s}: {cnt} studies")
    print(f"\nILUC included : {df['include_iluc'].sum()} studies")
    print(f"{'='*60}\n")


if __name__ == "__main__":
    summary()
